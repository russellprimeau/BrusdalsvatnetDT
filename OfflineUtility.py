        with c2:
            m3 = st.number_input("# Number of process parameters written", value=2, step=1)
            m4 = st.number_input("# Number of map output time steps written", value=1, step=1)
        map_size = est_map_size(Ndxi=Ndxi, Kmax=Kmax, m3=m3, m4=m4)
